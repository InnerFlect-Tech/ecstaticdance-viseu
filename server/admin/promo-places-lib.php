<?php
declare(strict_types=1);

/**
 * Locais de promoção — lista mestre de sítios onde divulgamos cada edição
 * (agendas culturais, grupos de Facebook/WhatsApp/Telegram, Instagram, cartazes…).
 *
 * A lista persiste entre edições (tabela `promo_places`). Para cada edição
 * marcamos quando publicámos em cada local (tabela `promo_place_posts`,
 * uma linha por par local↔evento). Usada por server/admin/promotion.php.
 */

const EDV_PROMO_PLACE_TYPES = [
    'agenda'    => 'Agenda cultural',
    'fb_grupo'  => 'Grupo Facebook',
    'fb_pagina' => 'Página Facebook',
    'whatsapp'  => 'Grupo WhatsApp',
    'telegram'  => 'Grupo Telegram',
    'instagram' => 'Instagram',
    'cartaz'    => 'Cartaz / presencial',
    'outro'     => 'Outro',
];

function edv_promo_places_now_sql(): string
{
    return date('Y-m-d H:i:s');
}

function edv_promo_places_ensure_schema(PDO $pdo): void
{
    $driver = (string) $pdo->getAttribute(PDO::ATTR_DRIVER_NAME);
    if ($driver === 'mysql') {
        $pdo->exec(
            "CREATE TABLE IF NOT EXISTS promo_places (
                id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
                name VARCHAR(160) NOT NULL,
                type VARCHAR(20) NOT NULL DEFAULT 'outro',
                url VARCHAR(500) NULL,
                notes TEXT NULL,
                is_active TINYINT(1) NOT NULL DEFAULT 1,
                sort_order INT NOT NULL DEFAULT 0,
                created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP
            ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci"
        );
        $pdo->exec(
            "CREATE TABLE IF NOT EXISTS promo_place_posts (
                id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
                place_id BIGINT UNSIGNED NOT NULL,
                event_id BIGINT UNSIGNED NOT NULL,
                posted_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
                notes VARCHAR(255) NULL,
                UNIQUE KEY uq_place_event (place_id, event_id),
                KEY idx_event (event_id)
            ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci"
        );
    } elseif ($driver === 'pgsql') {
        $pdo->exec(
            "CREATE TABLE IF NOT EXISTS promo_places (
                id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                name VARCHAR(160) NOT NULL,
                type VARCHAR(20) NOT NULL DEFAULT 'outro',
                url VARCHAR(500) NULL,
                notes TEXT NULL,
                is_active SMALLINT NOT NULL DEFAULT 1,
                sort_order INTEGER NOT NULL DEFAULT 0,
                created_at TIMESTAMP WITHOUT TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP
            )"
        );
        $pdo->exec(
            "CREATE TABLE IF NOT EXISTS promo_place_posts (
                id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                place_id BIGINT NOT NULL,
                event_id BIGINT NOT NULL,
                posted_at TIMESTAMP WITHOUT TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
                notes VARCHAR(255) NULL,
                CONSTRAINT uq_place_event UNIQUE (place_id, event_id)
            )"
        );
        $pdo->exec("CREATE INDEX IF NOT EXISTS idx_promo_post_event ON promo_place_posts (event_id)");
    } else {
        $pdo->exec(
            "CREATE TABLE IF NOT EXISTS promo_places (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                name TEXT NOT NULL,
                type TEXT NOT NULL DEFAULT 'outro',
                url TEXT NULL,
                notes TEXT NULL,
                is_active INTEGER NOT NULL DEFAULT 1,
                sort_order INTEGER NOT NULL DEFAULT 0,
                created_at TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP
            )"
        );
        $pdo->exec(
            "CREATE TABLE IF NOT EXISTS promo_place_posts (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                place_id INTEGER NOT NULL,
                event_id INTEGER NOT NULL,
                posted_at TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP,
                notes TEXT NULL,
                UNIQUE (place_id, event_id)
            )"
        );
        $pdo->exec("CREATE INDEX IF NOT EXISTS idx_promo_post_event ON promo_place_posts (event_id)");
    }
}

function edv_promo_place_type_label(string $type): string
{
    return EDV_PROMO_PLACE_TYPES[$type] ?? ucfirst($type);
}

/** Starter list of channels (idempotent). URLs reais ficam por preencher onde não confirmadas. */
function edv_promo_places_seed(PDO $pdo): void
{
    $count = (int) $pdo->query('SELECT COUNT(*) FROM promo_places')->fetchColumn();
    if ($count > 0) {
        return;
    }

    // [name, type, url, notes]
    $rows = [
        ['Viral Agenda', 'agenda', 'https://www.viralagenda.com', 'Submeter o evento na agenda cultural (alcance regional).'],
        ['Agenda Cultural de Viseu', 'agenda', '', 'Agenda da Câmara / cidade — confirmar o canal de submissão.'],
