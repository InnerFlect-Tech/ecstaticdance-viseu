        ['Facebook — Página Ecstatic Dance Viseu', 'fb_pagina', '', 'Publicar evento + posts das 4 fases.'],
        ['Facebook — grupos de dança/consciência (Viseu/Centro)', 'fb_grupo', '', 'Partilhar em grupos locais de dança, bem-estar e eventos.'],
        ['WhatsApp — grupo da 1ª edição', 'whatsapp', '', 'Comunidade que já dançou connosco (com código de regresso).'],
        ['WhatsApp — facilitadores / staff', 'whatsapp', '', 'Grupo de coordenação interna.'],
        ['Telegram — comunidade ecstatic dance', 'telegram', '', 'Canais/grupos de dança e eventos conscientes.'],
        ['Cartazes — Viseu (cafés, estúdios de yoga, lojas bio)', 'cartaz', '', 'Distribuição física em pontos de passagem da comunidade.'],
    ];

    $stmt = $pdo->prepare(
        'INSERT INTO promo_places (name, type, url, notes, is_active, sort_order, created_at)
         VALUES (?, ?, ?, ?, 1, ?, ?)'
    );
    $now = edv_promo_places_now_sql();
