$summary = ['total_costs' => 0.0, 'reimbursed_costs' => 0.0, 'pending_reimburse' => 0.0, 'promised_costs' => 0.0];
if ($selectedEvent > 0) {
    $c = $pdo->prepare('SELECT * FROM event_costs WHERE event_id = ? ORDER BY incurred_at DESC, id DESC');
    $c->execute([$selectedEvent]);
    $costs = $c->fetchAll();

