function edv_link_seed_uuid_for_ref(string $paymentRef): string
{
    $hex = substr(hash('sha256', 'edv-link-' . EDV_LINK_EVENT_SLUG_2026_05_23 . '-' . $paymentRef), 0, 32);
    $hex[12] = '4';
    $hex[16] = dechex((hexdec($hex[16]) & 0x3) | 0x8);

    return vsprintf('%s%s-%s-%s-%s-%s%s%s', str_split($hex, 4));
}

/**
 * Remove inscrições de teste/duplicadas e grava a lista de produção para edv-2026-05-23.
 *
 * @return array{deleted: int, inserted: int}
 */
function edv_link_registrations_apply_2026_05_23_seed(PDO $pdo): array
{
    if ($pdo->getAttribute(PDO::ATTR_DRIVER_NAME) === 'sqlite') {
        link_sqlite_migrate($pdo);
    } else {
        link_registrations_ensure_columns($pdo);
    }

    $slug = EDV_LINK_EVENT_SLUG_2026_05_23;
    $del  = $pdo->prepare('DELETE FROM link_registrations WHERE event_slug = ?');
    $del->execute([$slug]);
    $deleted = $del->rowCount();

    $ins = $pdo->prepare(
        'INSERT INTO link_registrations
         (id, payment_ref, event_slug, name, email, phone, ticket_euros, dinner_note, total_euros,
          payment_method, heard_from, heard_other, step1_at, step2_type, proof_relpath, proof_mime,
          step2_at, ticket_id, confirmed_at, receipt_email_sent_at, created_at, updated_at)
         VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, NULL, ?, ?, NULL, NULL, ?, NULL, NULL, NULL, ?, ?)'
    );

    $inserted = 0;
    foreach (edv_link_registrations_2026_05_23_rows() as $row) {
        $step1 = (string) $row['step1_at'];
        $ins->execute([
            edv_link_seed_uuid_for_ref((string) $row['payment_ref']),
            (string) $row['payment_ref'],
            $slug,
            (string) $row['name'],
            (string) $row['email'],
            (string) $row['phone'],
            (float) $row['ticket_euros'],
            '',
            (float) $row['total_euros'],
            (string) $row['payment_method'],
            (string) $row['heard_from'],
            $step1,
            (string) $row['step2_type'],
            (string) $row['step2_at'],
            $step1,
            $step1,
        ]);
        $inserted++;
    }

    return ['deleted' => $deleted, 'inserted' => $inserted];
}
