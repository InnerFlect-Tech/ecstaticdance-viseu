        $stmt->execute([
            mb_substr($r[0], 0, 160),
            $r[1],
            $r[2] !== '' ? $r[2] : null,
            $r[3] !== '' ? $r[3] : null,
            $i,
            $now,
        ]);
    }
}

/** @return array<int,array<string,mixed>> All places (optionally only active). */
function edv_promo_places_all(PDO $pdo, bool $onlyActive = false): array
{
    $sql = 'SELECT * FROM promo_places';
    if ($onlyActive) {
        $sql .= ' WHERE is_active = 1';
    }
    $sql .= ' ORDER BY is_active DESC, sort_order ASC, name ASC';
    return $pdo->query($sql)->fetchAll(PDO::FETCH_ASSOC) ?: [];
}

function edv_promo_place_create(PDO $pdo, string $name, string $type, string $url, string $notes): void
{
    if (!isset(EDV_PROMO_PLACE_TYPES[$type])) {
        $type = 'outro';
    }
    $maxOrder = (int) $pdo->query('SELECT COALESCE(MAX(sort_order), 0) FROM promo_places')->fetchColumn();
    $stmt = $pdo->prepare(
        'INSERT INTO promo_places (name, type, url, notes, is_active, sort_order, created_at)
         VALUES (?, ?, ?, ?, 1, ?, ?)'
    );
    $stmt->execute([
        mb_substr($name, 0, 160),
        $type,
        $url !== '' ? mb_substr($url, 0, 500) : null,
        $notes !== '' ? $notes : null,
        $maxOrder + 1,
        edv_promo_places_now_sql(),
    ]);
}

function edv_promo_place_update(PDO $pdo, int $id, string $name, string $type, string $url, string $notes): void
{
    if (!isset(EDV_PROMO_PLACE_TYPES[$type])) {
        $type = 'outro';
    }
    $stmt = $pdo->prepare('UPDATE promo_places SET name=?, type=?, url=?, notes=? WHERE id=?');
    $stmt->execute([
        mb_substr($name, 0, 160),
        $type,
        $url !== '' ? mb_substr($url, 0, 500) : null,
        $notes !== '' ? $notes : null,
        $id,
    ]);
}

function edv_promo_place_toggle_active(PDO $pdo, int $id): void
{
    $cur = $pdo->prepare('SELECT is_active FROM promo_places WHERE id=?');
    $cur->execute([$id]);
    $val = $cur->fetchColumn();
    if ($val === false) {
        return;
    }
    $new = ((int) $val) === 1 ? 0 : 1;
    $pdo->prepare('UPDATE promo_places SET is_active=? WHERE id=?')->execute([$new, $id]);
}

function edv_promo_place_delete(PDO $pdo, int $id): void
{
    $pdo->prepare('DELETE FROM promo_place_posts WHERE place_id=?')->execute([$id]);
    $pdo->prepare('DELETE FROM promo_places WHERE id=?')->execute([$id]);
}

/**
 * Toggle "publicado neste local para esta edição". Returns true if now marked,
 * false if unmarked.
 */
function edv_promo_place_toggle_posted(PDO $pdo, int $placeId, int $eventId): bool
{
    $q = $pdo->prepare('SELECT id FROM promo_place_posts WHERE place_id=? AND event_id=?');
    $q->execute([$placeId, $eventId]);
    $existing = $q->fetchColumn();
    if ($existing !== false) {
        $pdo->prepare('DELETE FROM promo_place_posts WHERE id=?')->execute([(int) $existing]);
        return false;
    }
    $pdo->prepare('INSERT INTO promo_place_posts (place_id, event_id, posted_at) VALUES (?, ?, ?)')
        ->execute([$placeId, $eventId, edv_promo_places_now_sql()]);
    return true;
}

/** @return array<int,string> place_id => posted_at (Y-m-d H:i:s) for one event. */
function edv_promo_place_posts_for_event(PDO $pdo, int $eventId): array
{
    $q = $pdo->prepare('SELECT place_id, posted_at FROM promo_place_posts WHERE event_id=?');
    $q->execute([$eventId]);
    $map = [];
    foreach ($q->fetchAll(PDO::FETCH_ASSOC) as $r) {
        $map[(int) $r['place_id']] = (string) $r['posted_at'];
    }
    return $map;
}
