function costs_now_sql(): string {
    return date('Y-m-d H:i:s');
}

function costs_h(string $value): string {
    return htmlspecialchars($value, ENT_QUOTES | ENT_SUBSTITUTE, 'UTF-8');
}

function costs_ensure_table(PDO $pdo): void {
    $driver = (string)$pdo->getAttribute(PDO::ATTR_DRIVER_NAME);
    if ($driver === 'mysql') {
        $pdo->exec(
            "CREATE TABLE IF NOT EXISTS event_costs (
                id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
                event_id INT UNSIGNED NOT NULL,
                label VARCHAR(255) NOT NULL,
                category VARCHAR(80) NOT NULL DEFAULT '',
                amount_eur DECIMAL(10,2) NOT NULL,
                paid_by VARCHAR(120) NULL,
                notes TEXT NULL,
                incurred_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
                reimbursed TINYINT(1) NOT NULL DEFAULT 0,
                cost_stage VARCHAR(16) NOT NULL DEFAULT 'actual',
                reimbursed_at DATETIME NULL,
                created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
                KEY idx_event (event_id),
                KEY idx_incurred_at (incurred_at)
            ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci"
        );
        try {
            $pdo->exec("ALTER TABLE event_costs ADD COLUMN cost_stage VARCHAR(16) NOT NULL DEFAULT 'actual'");
        } catch (Throwable $e) {
            // Column may already exist.
        }
        return;
    }
    if ($driver === 'pgsql') {
        $pdo->exec(
            "CREATE TABLE IF NOT EXISTS event_costs (
                id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                event_id INTEGER NOT NULL,
                label VARCHAR(255) NOT NULL,
                category VARCHAR(80) NOT NULL DEFAULT '',
                amount_eur NUMERIC(10,2) NOT NULL,
                paid_by VARCHAR(120) NULL,
                notes TEXT NULL,
                incurred_at TIMESTAMP WITHOUT TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
                reimbursed BOOLEAN NOT NULL DEFAULT FALSE,
                cost_stage VARCHAR(16) NOT NULL DEFAULT 'actual',
                reimbursed_at TIMESTAMP WITHOUT TIME ZONE NULL,
                created_at TIMESTAMP WITHOUT TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP
            )"
        );
        $pdo->exec("CREATE INDEX IF NOT EXISTS idx_event_costs_event ON event_costs (event_id)");
        $pdo->exec("CREATE INDEX IF NOT EXISTS idx_event_costs_incurred ON event_costs (incurred_at)");
        try {
            $pdo->exec("ALTER TABLE event_costs ADD COLUMN cost_stage VARCHAR(16) NOT NULL DEFAULT 'actual'");
        } catch (Throwable $e) {
            // Column may already exist.
        }
        return;
    }
    $pdo->exec(
        "CREATE TABLE IF NOT EXISTS event_costs (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            event_id INTEGER NOT NULL,
            label TEXT NOT NULL,
            category TEXT NOT NULL DEFAULT '',
            amount_eur REAL NOT NULL,
            paid_by TEXT NULL,
            notes TEXT NULL,
            incurred_at TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP,
            reimbursed INTEGER NOT NULL DEFAULT 0,
            cost_stage TEXT NOT NULL DEFAULT 'actual',
            reimbursed_at TEXT NULL,
            created_at TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP
        )"
    );
    $pdo->exec("CREATE INDEX IF NOT EXISTS idx_event_costs_event ON event_costs (event_id)");
    $pdo->exec("CREATE INDEX IF NOT EXISTS idx_event_costs_incurred ON event_costs (incurred_at)");
    try {
        $pdo->exec("ALTER TABLE event_costs ADD COLUMN cost_stage TEXT NOT NULL DEFAULT 'actual'");
    } catch (Throwable $e) {
        // Column may already exist.
    }
}

$pdo = db();
costs_ensure_table($pdo);

$flash = '';
$selectedEvent = (int)($_REQUEST['event_id'] ?? 0);
$editCostId = (int)($_REQUEST['edit_id'] ?? 0);

if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $action = (string)($_POST['action'] ?? '');
    if ($action === 'add_cost') {
        $eventId = (int)($_POST['event_id'] ?? 0);
        $label = trim((string)($_POST['label'] ?? ''));
        $category = trim((string)($_POST['category'] ?? ''));
        $amount = (float)($_POST['amount_eur'] ?? 0);
        $paidBy = trim((string)($_POST['paid_by'] ?? ''));
        $notes = trim((string)($_POST['notes'] ?? ''));
        $incurredAt = trim((string)($_POST['incurred_at'] ?? ''));
        $costStage = (string)($_POST['cost_stage'] ?? 'actual');
        if (!in_array($costStage, ['actual', 'promised'], true)) {
            $costStage = 'actual';
        }

        if ($eventId <= 0 || $label === '' || $amount <= 0) {
            $flash = 'Preenche evento, descrição e valor (>0).';
        } else {
            if ($incurredAt === '') {
                $incurredAt = costs_now_sql();
            }
            $stmt = $pdo->prepare(
                'INSERT INTO event_costs (event_id, label, category, amount_eur, paid_by, notes, incurred_at, reimbursed, cost_stage, reimbursed_at, created_at)
                 VALUES (?, ?, ?, ?, ?, ?, ?, 0, ?, NULL, ?)'
            );
            $stmt->execute([
                $eventId,
                mb_substr($label, 0, 255),
                mb_substr($category, 0, 80),
                $amount,
                $paidBy !== '' ? mb_substr($paidBy, 0, 120) : null,
                $notes !== '' ? $notes : null,
                $incurredAt,
                $costStage,
                costs_now_sql(),
            ]);
            $flash = 'Custo registado.';
            $selectedEvent = $eventId;
            $editCostId = 0;
        }
    } elseif ($action === 'update_cost') {
        $id = (int)($_POST['id'] ?? 0);
        $eventId = (int)($_POST['event_id'] ?? 0);
        $label = trim((string)($_POST['label'] ?? ''));
        $category = trim((string)($_POST['category'] ?? ''));
        $amount = (float)($_POST['amount_eur'] ?? 0);
        $paidBy = trim((string)($_POST['paid_by'] ?? ''));
        $notes = trim((string)($_POST['notes'] ?? ''));
        $incurredAt = trim((string)($_POST['incurred_at'] ?? ''));
        $costStage = (string)($_POST['cost_stage'] ?? 'actual');
        if (!in_array($costStage, ['actual', 'promised'], true)) {
            $costStage = 'actual';
        }
        if ($id <= 0 || $eventId <= 0 || $label === '' || $amount <= 0) {
            $flash = 'Preenche evento, descrição e valor (>0).';
            $editCostId = $id;
        } else {
            if ($incurredAt === '') {
                $incurredAt = costs_now_sql();
            }
            $stmt = $pdo->prepare(
                'UPDATE event_costs
                 SET label = ?, category = ?, amount_eur = ?, paid_by = ?, notes = ?, incurred_at = ?, cost_stage = ?
                 WHERE id = ? AND event_id = ?'
            );
            $stmt->execute([
                mb_substr($label, 0, 255),
                mb_substr($category, 0, 80),
                $amount,
                $paidBy !== '' ? mb_substr($paidBy, 0, 120) : null,
                $notes !== '' ? $notes : null,
                $incurredAt,
                $costStage,
                $id,
                $eventId,
            ]);
            $flash = 'Custo atualizado.';
            $selectedEvent = $eventId;
            $editCostId = 0;
        }
    } elseif ($action === 'delete_cost') {
        $id = (int)($_POST['id'] ?? 0);
        $eventId = (int)($_POST['event_id'] ?? 0);
        if ($id > 0) {
            $stmt = $pdo->prepare('DELETE FROM event_costs WHERE id = ?');
            $stmt->execute([$id]);
            $flash = 'Custo apagado.';
        }
        $selectedEvent = $eventId;
        if ($editCostId === $id) {
            $editCostId = 0;
        }
    } elseif ($action === 'toggle_reimbursed') {
        $id = (int)($_POST['id'] ?? 0);
        $eventId = (int)($_POST['event_id'] ?? 0);
        $newValue = ((int)($_POST['reimbursed'] ?? 0)) === 1 ? 1 : 0;
        $stmt = $pdo->prepare(
            'UPDATE event_costs
             SET reimbursed = ?, reimbursed_at = ?
             WHERE id = ?'
        );
        $stmt->execute([$newValue, $newValue === 1 ? costs_now_sql() : null, $id]);
        $flash = $newValue === 1 ? 'Marcado como reembolsado.' : 'Marcado como pendente.';
        $selectedEvent = $eventId;
    }
}

$eventsStmt = $pdo->query(
    "SELECT e.id, e.title, e.date, e.capacity, e.min_price,
            COALESCE(SUM(CASE WHEN t.payment_status = 'paid' THEN t.amount_paid ELSE 0 END), 0) AS revenue_paid,
            COALESCE(SUM(CASE WHEN t.payment_status IN ('paid','free') THEN 1 ELSE 0 END), 0) AS tickets_sold,
            COALESCE(SUM(CASE WHEN t.payment_status = 'paid' THEN 1 ELSE 0 END), 0) AS tickets_paid
     FROM events e
     LEFT JOIN tickets t ON t.event_id = e.id
     GROUP BY e.id
     ORDER BY e.date DESC
     LIMIT 30"
);
$events = $eventsStmt->fetchAll();
if ($selectedEvent <= 0 && !empty($events)) {
    $selectedEvent = (int)$events[0]['id'];
}

$selected = null;
